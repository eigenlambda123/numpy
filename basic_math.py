import numpy as np

a = np.array([1,2,3,4])
b = np.array([1,0,1,0])
print(a)

# Arithmetic
print(a + 2)
print(a - 2)
print(a * 2)
print(a / 2)
print(a+b)
print(a**2)

# Trigonometry
print(np.sin(a))
print(np.cos(a))

